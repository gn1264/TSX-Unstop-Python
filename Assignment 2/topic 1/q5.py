
# Step 1: Store your name in a variable
name = "Gaurav"  # You can change this to your own name

# Step 2: Create a greeting message
greeting = "Hello"  # This is a simple greeting

# Step 3: Combine greeting and name to form a full message
message = greeting + ", " + name + "!"  # The '+' operator joins the strings

# Step 4: Print the final message to the screen
print(message)  # This will output something like: Hello, Gaurav!
